//赋值运算符
//赋值运算符：符号"="，表明将右边的结果(可以是变量，数据，常量和其他均运算出来的结果)，保存到内存某个位置；
//然后将位置的内存地址赋值给左侧的变量


//算术运算符
//算术运算，基本算术操作
// +:
// -:
